function escapeRegExp(text = '') {
  return String(text).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

export function sanitizePolicyText(text = '') {
  let output = String(text || '');

  // Regex first: catches sentence-level risky patterns like “ลด 10 โลใน 7 วัน”.
  for (const rule of POLICY_REGEX_RULES) {
    output = output.replace(rule.pattern, rule.replacement);
  }

  // Replace longer phrases first so “100% เห็นผล” is not partially replaced by “100%”.
  const terms = [...new Set(POLICY_BANNED_TERMS)].sort((a, b) => b.length - a.length);
  for (const term of terms) {
    const replacement = POLICY_REPLACEMENTS[term] || '';
    const pattern = new RegExp(escapeRegExp(term), 'gi');
    output = output.replace(pattern, replacement);
  }

  return output
    .replace(/\s{2,}/g, ' ')
    .replace(/\s+([,.!?。])/g, '$1')
    .trim();
}
